def find_symbols(dream_text: str) -> list[dict]:
    """在夢境描述中尋找已知的夢境符號"""
    found = []
    for symbol_name, symbol_data in DREAM_SYMBOLS.items():
        for keyword in symbol_data["keywords"]:
            if keyword in dream_text:
                found.append(
                    {
                        "name": symbol_name,
                        "psychological": symbol_data["psychological"],
                        "mystical": symbol_data["mystical"],
                        "emotion": symbol_data["emotion"],
                    }
                )
                break
    return found
